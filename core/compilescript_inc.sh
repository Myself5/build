bakwht='\e[47m'   # White
txtrst='\e[0m'    # Text Reset

setvar() {
    NAME="$1"
    VALUE="$2"
    echo "export $NAME=\"$VALUE\"" >> "$OUT/buildtime_variables.sh"
    echo "$NAME = \"$VALUE\"" >> "$OUT/buildtime_variables.py"
    export "$NAME"="$VALUE"
}

pr_error() {
    echo -e "${bldred}$@${txtrst}"
}
pr_fatal() {
    pr_error "$@"
    exit 1
}
pr_info() {
    echo -e "${bldwht}$@${txtrst}"
}
pr_warning() {
    echo -e "${bldylw}$@${txtrst}"
}
pr_notice() {
    echo -e "${bldcyn}$@${txtrst}"
}
pr_alert() {
    echo -e "${bldgrn}$@${txtrst}"
}
